namespace Telerik.UI.Xaml.Controls.Input
{
    /// <summary>
    /// Defines the selection behavior for a RadCalendar.
    /// </summary>
    public enum CalendarSelectionMode
    {
        /// <summary>
        /// Defines the selection behavior for a RadCalendar.
        /// </summary>
        None = 0,

        /// <summary>
        /// The user can select only one item at a time.
        /// </summary>
        Single = 1,

        /// <summary>
        ///  The user can select multiple items by holding down a modifier key or by holding pointer (touch environment).
        /// </summary>
        Multiple = 2,
    }
}
