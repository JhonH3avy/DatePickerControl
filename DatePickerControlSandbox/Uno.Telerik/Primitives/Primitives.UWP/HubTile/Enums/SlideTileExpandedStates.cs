using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Telerik.UI.Xaml.Controls.Primitives
{
    /// <summary>
    /// Defines the Expanded State options for a <see cref="RadSlideHubTile"/>.
    /// </summary>
    public enum SlideTileExpandedState
    {
        /// <summary>
        /// Normal, or neutral state.
        /// </summary>
        Normal,

        /// <summary>
        /// Both of the contents are partially visible.
        /// </summary>
        SemiExpanded,

        /// <summary>
        /// Only the second content is visible.
        /// </summary>
        Expanded
    }
}
