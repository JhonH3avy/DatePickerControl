namespace Telerik.UI.Xaml.Controls.Primitives
{
    /// <summary>
    /// Specifies the zoom mode of the image.
    /// </summary>
    public enum ImageZoomMode
    {
        /// <summary>
        /// Specifies a "None" image zoom mode.
        /// </summary>
        None = 0,

        /// <summary>
        /// Specifies a "Free" image zoom mode.
        /// </summary>
        Free = 1,

        /// <summary>
        /// Specifies a "FitToPhysicalSize" image zoom mode.
        /// </summary>
        FitToPhysicalSize = 2,
    }
}